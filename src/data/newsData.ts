export const getNewsById = (id: string): NewsItem | undefined => {
  return allNews.find(news => news.id === id);
};

export const getRelatedNews = (currentId: string, categorySlug: string, limit: number = 4): NewsItem[] => {
  return allNews
    .filter(news => news.id !== currentId && news.categorySlug === categorySlug)
    .slice(0, limit);
};

export const getPopularNews = (limit: number = 5): NewsItem[] => {
  return [...allNews].sort((a, b) => b.views - a.views).slice(0, limit);
};

export const getLatestNews = (limit: number = 10): NewsItem[] => {
  return allNews.slice(0, limit);
};
